"use strict"

// game config
let config = {
    parent: 'phaser-game',
    type: Phaser.CANVAS,
    render: {
        pixelArt: true  // prevent pixel art from getting blurred when scaled
    },
    width: 832,
    height: 832,
    scene: [TutorialScene, Main],
    backgroundColor: 0x079D68
}

const game = new Phaser.Game(config);